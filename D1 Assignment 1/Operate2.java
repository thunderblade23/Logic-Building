class Operate2
{
public static void main(String args[])
	{
	int a = 125; // Define a
	int b = 24; // Define b
	int c = a + b;
	int d = a - b;
	int e = a * b;
	int f = a / b;
	int g = a % b;
	System.out.println("125 + 24 = " + c + "\n");
	System.out.println("125 - 24 = " + d + "\n");
	System.out.println("125 * 24 = " + e + "\n");
	System.out.println("125 / 24 = " + f + "\n");
	System.out.println("125 mod 24 = " + g + "\n");
	}
}