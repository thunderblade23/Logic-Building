class EvenOddCheck 
{
public static void main(String[] args) 
	{        
        int number = 15; // Change this value to check for a different number
        
        if (number % 2 == 0) 
		{
            	System.out.println(number + " is an even number.");
        	} 
	else 
		{
            	System.out.println(number + " is an odd number.");
        	}
    	}
}
